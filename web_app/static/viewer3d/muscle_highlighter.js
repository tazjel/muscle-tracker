/**
 * muscle_highlighter.js — Interactive muscle group vertex-color highlighting
 *
 * Usage:
 *   import * as THREE from 'three';
 *   import { MuscleHighlighter } from './muscle_highlighter.js';
 *   const highlighter = new MuscleHighlighter(THREE);
 *   // After GLB is loaded:
 *   highlighter.attach(bodyMesh);
 *   // Highlight a group:
 *   highlighter.highlight('biceps_l');
 *   // Clear all highlights:
 *   highlighter.clear();
 *
 * Requires Three.js r160+.
 * Does NOT use onBeforeCompile or custom shaders — uses vertex colors
 * with MeshStandardMaterial, which works on all devices including mobile.
 */
import * as THREE from 'https://cdn.jsdelivr.net/npm/three@0.160.0/build/three.module.js';

// ── Body segmentation vertex groups ──────────────────────────────────────────
// Source: template_vert_segmentation.json (gtd3d custom MPFB2 mesh, 13380 verts)
// Generated by scripts/blender_create_template.py from MakeHuman joint landmarks.
// Keys are fitness muscle group names directly — no mapping needed.

let _segData = null;
async function _loadSegmentation() {
  if (_segData) return _segData;
  const url = new URL('template_vert_segmentation.json', import.meta.url).href;
  const resp = await fetch(url);
  _segData = await resp.json();
  return _segData;
}

// Built at runtime from loaded JSON
let MUSCLE_GROUPS = {};

// Human-readable display names
const MUSCLE_LABELS = {
  biceps_l:   'Biceps (L)',
  biceps_r:   'Biceps (R)',
  pectorals:  'Pectorals',
  abs:        'Abs',
  obliques:   'Obliques',
  deltoids_l: 'Deltoids (L)',
  deltoids_r: 'Deltoids (R)',
  traps:      'Trapezius',
  glutes:     'Glutes',
  quads_l:    'Quads (L)',
  quads_r:    'Quads (R)',
  calves_l:   'Calves (L)',
  calves_r:   'Calves (R)',
  forearms_l: 'Forearms (L)',
  forearms_r: 'Forearms (R)',
};

// Highlight color (RGB 0-1): warm glow that tints without destroying texture
const HIGHLIGHT_COLOR = { r: 1.4, g: 0.85, b: 0.7 };
// Dimmed color for non-highlighted areas (subtle desaturation, not aggressive darkening)
const DEFAULT_COLOR   = { r: 0.7, g: 0.7, b: 0.75 };
// Normal color when no highlighting is active
const NEUTRAL_COLOR   = { r: 1.0, g: 1.0, b: 1.0 };

// ── MuscleHighlighter class ──────────────────────────────────────────────────

export class MuscleHighlighter {
  constructor() {
    this._mesh = null;
    this._colorAttr = null;          // THREE.BufferAttribute for colors
    this._vertexCount = 0;
    this._activeGroup = null;
    this._enabled = false;
  }


  /**
   * Attach to a loaded GLB scene. Call this after bodyMesh is set.
   * @param {THREE.Object3D} sceneRoot — the gltf.scene object
   */
  async attach(sceneRoot) {
    if (!sceneRoot) return;

    // Load real Meshcapade segmentation data
    const seg = await _loadSegmentation();
    // Segmentation JSON keys are muscle group names directly
    for (const [group, indices] of Object.entries(seg)) {
      if (Array.isArray(indices) && indices.length > 0) {
        MUSCLE_GROUPS[group] = indices;
      }
    }
    console.log(`[MuscleHighlighter] Loaded ${Object.keys(MUSCLE_GROUPS).length} groups (${Object.values(MUSCLE_GROUPS).reduce((s,a)=>s+a.length,0)} total verts)`);

    // Find the first mesh with enough vertices to be the body
    sceneRoot.traverse((child) => {
      if (this._mesh) return;
      if (child.isMesh && child.geometry) {
        const count = child.geometry.attributes.position.count;
        if (count >= 6000) {   // SMPL has 6890 — anything smaller is accessories
          this._mesh = child;
          this._vertexCount = count;
        }
      }
    });

    if (!this._mesh) {
      console.warn('[MuscleHighlighter] No suitable mesh found (need ≥6000 vertices)');
      return;
    }

    // Validate vertex count against segmentation data
    const maxIdx = Object.values(MUSCLE_GROUPS).reduce((mx, arr) => Math.max(mx, ...arr), 0);
    if (this._vertexCount <= maxIdx) {
      console.warn(`[MuscleHighlighter] Vertex count mismatch: mesh has ${this._vertexCount} verts but segmentation references index ${maxIdx}. Highlights may be incorrect.`);
    } else {
      console.log(`[MuscleHighlighter] Attached to mesh with ${this._vertexCount} vertices (segmentation max index: ${maxIdx})`);
    }

    // Add vertex color attribute (default white)
    const colors = new Float32Array(this._vertexCount * 3).fill(1.0);
    this._colorAttr = new THREE.BufferAttribute(colors, 3);
    this._mesh.geometry.setAttribute('color', this._colorAttr);

    // Enable vertex colors on the material
    if (Array.isArray(this._mesh.material)) {
      this._mesh.material.forEach(m => { m.vertexColors = true; m.needsUpdate = true; });
    } else {
      this._mesh.material.vertexColors = true;
      this._mesh.material.needsUpdate = true;
    }

    this._enabled = true;
    console.log(`[MuscleHighlighter] Attached to mesh with ${this._vertexCount} vertices`);
  }

  /**
   * Highlight a muscle group by key.
   * @param {string} groupKey — key from MUSCLE_GROUPS
   */
  highlight(groupKey) {
    if (!this._enabled) return;

    const indices = MUSCLE_GROUPS[groupKey];
    if (!indices) {
      console.warn(`[MuscleHighlighter] Unknown group: ${groupKey}`);
      return;
    }

    // Ensure vertex colors are enabled on current material (may have been replaced)
    this._ensureVertexColors();

    // Reset all to default
    this._fillAll(DEFAULT_COLOR);

    // Paint the selected group
    const colors = this._colorAttr.array;
    for (const idx of indices) {
      if (idx < this._vertexCount) {
        colors[idx * 3]     = HIGHLIGHT_COLOR.r;
        colors[idx * 3 + 1] = HIGHLIGHT_COLOR.g;
        colors[idx * 3 + 2] = HIGHLIGHT_COLOR.b;
      }
    }

    this._colorAttr.needsUpdate = true;
    this._activeGroup = groupKey;
  }

  /** Clear all highlights (return to normal appearance). */
  clear() {
    if (!this._enabled) return;
    this._fillAll(NEUTRAL_COLOR);
    this._colorAttr.needsUpdate = true;
    // Disable vertexColors so texture renders normally without tint
    if (this._mesh) {
      if (Array.isArray(this._mesh.material)) {
        this._mesh.material.forEach(m => { m.vertexColors = false; m.needsUpdate = true; });
      } else if (this._mesh.material) {
        this._mesh.material.vertexColors = false;
        this._mesh.material.needsUpdate = true;
      }
    }
    this._activeGroup = null;
  }

  /** Disable vertex colors entirely (restore original material appearance). */
  detach() {
    if (!this._mesh) return;
    if (Array.isArray(this._mesh.material)) {
      this._mesh.material.forEach(m => { m.vertexColors = false; m.needsUpdate = true; });
    } else {
      this._mesh.material.vertexColors = false;
      this._mesh.material.needsUpdate = true;
    }
    this._enabled = false;
    this._activeGroup = null;
  }

  get activeGroup() { return this._activeGroup; }
  get isEnabled()   { return this._enabled; }

  /** @private Ensure vertexColors is enabled on current material (may have been replaced by PBR loader). */
  _ensureVertexColors() {
    if (!this._mesh) return;
    // Re-add color attribute if geometry lost it
    if (!this._mesh.geometry.attributes.color) {
      const colors = new Float32Array(this._vertexCount * 3).fill(1.0);
      this._colorAttr = new THREE.BufferAttribute(colors, 3);
      this._mesh.geometry.setAttribute('color', this._colorAttr);
    }
    // Enable vertexColors on current material
    if (Array.isArray(this._mesh.material)) {
      this._mesh.material.forEach(m => {
        if (!m.vertexColors) { m.vertexColors = true; m.needsUpdate = true; }
      });
    } else if (this._mesh.material && !this._mesh.material.vertexColors) {
      this._mesh.material.vertexColors = true;
      this._mesh.material.needsUpdate = true;
    }
  }

  /** @private Fill all vertices with a color. */
  _fillAll(color) {
    const colors = this._colorAttr.array;
    for (let i = 0; i < this._vertexCount; i++) {
      colors[i * 3]     = color.r;
      colors[i * 3 + 1] = color.g;
      colors[i * 3 + 2] = color.b;
    }
  }
}

