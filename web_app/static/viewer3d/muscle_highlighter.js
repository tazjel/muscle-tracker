/**
 * Build and inject a muscle group selector sidebar panel.
 * @param {MuscleHighlighter} highlighter
 * @param {HTMLElement} container — where to inject the panel
 */
export function buildMusclePanel(highlighter, container) {
  const panel = document.createElement('div');
  panel.id = 'muscle-panel';
  panel.style.cssText = `
    position: absolute; top: 60px; right: 12px;
    background: rgba(12,12,24,0.92); border-radius: 14px;
    padding: 14px 10px; display: flex; flex-direction: column;
    gap: 3px; min-width: 150px; z-index: 200;
    font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
    font-size: 13px; color: #fff;
    backdrop-filter: blur(16px); user-select: none;
    border: 1px solid rgba(74, 158, 255, 0.2);
    box-shadow: 0 8px 32px rgba(0,0,0,0.4);
    max-height: calc(100vh - 140px);
    overflow-y: auto;
  `;

  const title = document.createElement('div');
  title.textContent = 'MUSCLE GROUPS';
  title.style.cssText = 'font-weight:700; font-size:10px; color:#6b7fa8; margin-bottom:6px; text-align:center; letter-spacing:0.12em;';
  panel.appendChild(title);

  // Group muscles by region for visual organization
  const REGIONS = {
    'Upper Body': ['pectorals', 'traps', 'deltoids_l', 'deltoids_r'],
    'Arms': ['biceps_l', 'biceps_r', 'forearms_l', 'forearms_r'],
    'Core': ['abs', 'obliques'],
    'Lower Body': ['glutes', 'quads_l', 'quads_r', 'calves_l', 'calves_r'],
  };

  let activeBtn = null;

  for (const [region, keys] of Object.entries(REGIONS)) {
    const label = document.createElement('div');
    label.textContent = region;
    label.style.cssText = 'font-size:9px; color:#4a6a8f; font-weight:600; margin:6px 0 2px 4px; text-transform:uppercase; letter-spacing:0.08em;';
    panel.appendChild(label);

    for (const key of keys) {
      if (!MUSCLE_LABELS[key]) continue;
      const btn = document.createElement('button');
      btn.textContent = MUSCLE_LABELS[key];
      btn.dataset.group = key;
      btn.style.cssText = `
        background: rgba(255,255,255,0.06); border: 1px solid rgba(255,255,255,0.1);
        border-radius: 8px; color: #d0d8e8; padding: 8px 10px; cursor: pointer;
        text-align: left; font-size: 13px; transition: all 0.15s ease;
        font-weight: 500; min-height: 36px;
      `;
      btn.onmouseenter = () => {
        if (btn !== activeBtn) btn.style.background = 'rgba(255,255,255,0.12)';
      };
      btn.onmouseleave = () => {
        if (btn !== activeBtn) btn.style.background = 'rgba(255,255,255,0.06)';
      };
      btn.onclick = () => {
        if (activeBtn === btn) {
          highlighter.clear();
          btn.style.background = 'rgba(255,255,255,0.06)';
          btn.style.borderColor = 'rgba(255,255,255,0.1)';
          btn.style.color = '#d0d8e8';
          activeBtn = null;
        } else {
          if (activeBtn) {
            activeBtn.style.background = 'rgba(255,255,255,0.06)';
            activeBtn.style.borderColor = 'rgba(255,255,255,0.1)';
            activeBtn.style.color = '#d0d8e8';
          }
          highlighter.highlight(key);
          btn.style.background = 'rgba(255,80,50,0.3)';
          btn.style.borderColor = 'rgba(255,100,70,0.6)';
          btn.style.color = '#ff9070';
          activeBtn = btn;
        }
      };
      panel.appendChild(btn);
    }
  }

  // Clear button
  const clearBtn = document.createElement('button');
  clearBtn.textContent = 'Clear All';
  clearBtn.style.cssText = `
    margin-top: 8px; background: rgba(255,255,255,0.04);
    border: 1px solid rgba(255,255,255,0.08); border-radius: 8px;
    color: #6b7fa8; padding: 8px 10px; cursor: pointer; font-size: 12px;
    font-weight: 600; min-height: 36px;
  `;
  clearBtn.onclick = () => {
    highlighter.clear();
    if (activeBtn) {
      activeBtn.style.background = 'rgba(255,255,255,0.06)';
      activeBtn.style.borderColor = 'rgba(255,255,255,0.1)';
      activeBtn.style.color = '#d0d8e8';
      activeBtn = null;
    }
  };
  panel.appendChild(clearBtn);

  container.appendChild(panel);
  return panel;
}

